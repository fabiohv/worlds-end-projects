    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        Pessoa p = new Pessoa();
        
        AcessoDB banco = new AcessoDB();
        if (txtCodigo.isEnabled()){
            p.setNome(txtNome.getText());
            p.setTelefone(txtTelefone.getText());
            p.setLogradouro(txtLogradouro.getText());
            p.setNumero(Integer.parseInt(txtNumero.getText()));
            p.setComplemento(txtComplemento.getText());
            p.setBairro(txtBairro.getText());
            p.setCidade(txtCidade.getText());
            p.setCep(txtCep.getText());
            
            banco.insere(p);

            JOptionPane.showMessageDialog(null, "Pessoa inserida com sucesso!");
        }else{
            JOptionPane.showMessageDialog(null, "Limpe a tela e insira uma nova pessoa!");
        }
       
        preencheTabela("");
        
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        
        AcessoDB banco = new AcessoDB();
        
        if (tblPessoas.getSelectedRow() != -1){
            banco.deleta(Integer.parseInt(tableModel.getValueAt(tblPessoas.getSelectedRow(), 0).toString()));
            preencheTabela("");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void tblPessoasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPessoasMouseClicked
        int linha;
        
        linha = tblPessoas.getSelectedRow();
        
        if (linha != -1){
            txtCodigo.setText(tblPessoas.getValueAt(linha, 0).toString());
            txtNome.setText(tblPessoas.getValueAt(linha, 1).toString());
            txtTelefone.setText(tblPessoas.getValueAt(linha, 2).toString());
            txtLogradouro.setText(tblPessoas.getValueAt(linha, 3).toString());
            txtNumero.setText(tblPessoas.getValueAt(linha, 4).toString());
            txtComplemento.setText(tblPessoas.getValueAt(linha, 5).toString());
            txtBairro.setText(tblPessoas.getValueAt(linha, 6).toString());
            txtCidade.setText(tblPessoas.getValueAt(linha, 7).toString());
            txtCep.setText(tblPessoas.getValueAt(linha, 8).toString());
            txtCodigo.setEnabled(false);
        }
    }//GEN-LAST:event_tblPessoasMouseClicked

