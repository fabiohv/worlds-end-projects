    private void preencheTabela(String filtro){
        
        AcessoDB banco = new AcessoDB();
        tableModel.limpaTabela();
       
        for (Pessoa p: banco.busca(filtro)){
            tableModel.adicionaLinha(p);
        }
    }
    
    private void limpaCampos(){
        txtCodigo.setText("");
        txtNome.setText("");
        txtTelefone.setText("");
        txtLogradouro.setText("");
        txtNumero.setText("");
        txtComplemento.setText("");
        txtBairro.setText("");
        txtCidade.setText("");
        txtCep.setText("");
        txtCodigo.setEnabled(true);
    }
    
    private String validaCampos(){
        String validacao =  "";
        if ("".equals(txtNome.getText()))
           validacao = "É necessário digitar o Nome!";
        if ("".equals(txtTelefone.getText()))
            validacao = validacao + "É necessário digitar o Telefone!";
        if ("".equals(txtLogradouro.getText()))
            validacao = validacao + "É necessário digitar o Logradouro!";
        if ("".equals(txtNumero.getText()))
            validacao = validacao + "É necessário digitar o Numero!";
        if ("".equals(txtBairro.getText()))
            validacao = validacao + "É necessário digitar o Bairro!";
        if ("".equals(txtCidade.getText()))
            validacao = validacao + "É necessário digitar o Cidade!";
        if ("".equals(txtCep.getText()))
            validacao = validacao + "É necessário digitar o Cep!";
        return validacao;
    }
